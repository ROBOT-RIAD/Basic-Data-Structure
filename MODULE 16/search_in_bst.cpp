void insert(Node*& root ,int x)
{
    if(root == NULL)
    {
        root = new Node(x);
        return;
    }
    if (x < root->val)
    {
        if (root->left == NULL)
        {
            root->left = new Node(x);
        }
        else
        {
            insert(root->left, x);
        }
    }
    else
    {
        if (root->right == NULL)
        {
            root->right = new Node(x);
        }
        else
        {
            insert(root->right, x);
        }
    }
}

bool search(Node* root, int x)
{
    if(root == NULL) return false;
    if(root->val == x) return true;
    if( x<root->val)
       return search(root->left,x);
    else
       return search(root->right,x);
}

